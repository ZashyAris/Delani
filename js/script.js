$(document).ready(function(){
    $("#designImg").click(function(){
        $("#designImg").hide();
        $("#design").toggle();
        $("#design").show();
    });
    $("#design").click(function(){
        $("#design").hide();
        $("#designImg").toggle();
        $("#designImg").show();
    });
});
